/**
 * suma dos numeros
 * @param a
 * @param b
 * @returns {*}
 */
export const suma=(a,b)=>a+b;
/**
 * resta de dos numeros
 * @param a
 * @param b
 * @returns {number}
 */
export const resta=(a,b)=>a-b;
/**
 * Multiplicación de dos números
 * @param a
 * @param b
 * @returns {number}
 */
export const multiplicacion=(a,b)=>a*b;
